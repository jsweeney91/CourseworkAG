package pack1;

public class Graph {
	private String start;
	private String end;
	private String id;
	
	Graph(String id){
		
	}
	
	public String getStart() {
		return start;
	}
	public void setStart(String start) {
		this.start = start;
	}
	public String getEnd() {
		return end;
	}
	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public void setEnd(String end) {
		this.end = end;
	}
	
	
	
}
